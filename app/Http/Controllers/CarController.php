     */
    public function store(Request $request)
    {
        //

        $car = new Car;

        $car->car_name = $request->name;
        $car->manufacturer = $request->manufacturer;
        $car->type = $request->type;
        $car->color = $request->color;

        $car->save();

        return response()->json([
            "car" => $car
        ]);

    }

